                <h2 id="nadpis_pribeh">Z historie stolního tenisu</h2>
                <section class="rodic_pribeh">

                    <p>Stolní tenis, stejně jako i většina ostatních současných sportů vznikl a získal svá první pravidla v Anglii. První zmínka je z roku 1881, kdy si gentlemani, kteří z důvodu nepříznivého počasí nemohli hrát lawn tenis, krátili dlouhou chvíli hraním tenisu na stole. Jako pálky posloužila víka od krabic na doutníky, míček se vyrobil z korkové zátky a síťku nahradila řada knih. V roce 1890 přivezl z cesty po Americe James Gibb, který je mimochodem uznáván jako zakladatel závodní hry, celuloidový míček, který objevil mezi dětskými hračkami. Záhy se celuloid ukázal jako nejvhodnější materiál.</p>

                    <p>V Anglii se v prvních letech dvacátého století stal nový sport záhy velmi populární, ale jeho obliba zmizela stejně rychle jako přišla, protože nový sport kvůli materiálovému vybavení přinesl jen fádní pinkání. Ač byla již vynalezena pálka s vroubkovanou gumou, stolní tenis se na dvacet let propadl do nezájmu. V českých zemích se objevují první zmínky o stolním tenisu okolo roku 1900, po roce 1903 ale mizí. Teprve dvacátá léta minulého století znamenají znovuzrození stolního tenisu a jeho rozmach po celém světě.</p>

                    <p>První mistrovství světa se konalo v roce 1926 v Londýně. Toto mistrovství také ukončilo éru dřevěných pálek, které nahradila pálka s vroubkovanou gumou. Pro zajímavost, ještě na mistrovství někteří zastánci korku žádali zákaz gumových potahů s odůvodněním, že kazí hru. Historie se opakuje a o třicet let později chtěli vyznavači tehdy již klasické pálky zakázat houbové potahy a používali stejný argument.</p>

                    <p>Dvacátá léta opanovali zejména maďarští hráči a toto období historie stolního tenisu se obecně nazývá maďarská éra. Od roku 1932 ale sportu s celuloidovým míčkem začínají kralovat jiní hráči. Na mistrovství světa, které se konalo v Praze se našim v družstvech podařilo porazit do té doby suverénní Maďary a získat první titul mistrů světa. Od té doby se začala psát éra, která se v historii stolního tenisu označuje jako československá. Stolní tenis se v Československu stává počtem titulů mistrů světa jedním z nejúspěšnějších sportů. Celkem naši sportovci získali 29 titulů. Sluší se připomenout některá jména: Maleček, Kolář, Lauterbach, Váňa. Tereba, Šlár, Štípek, Andreadis, Tokár , mezi ženami Kettnerová, dále Votrubcová a Depetrisová, hráčky Viktorie Plzeň. Bohužel, titulů by bylo zřejmě mnohem víc, nebýt druhé světové války. Po ní sice ještě medaile cinkaly, ale po nástupu komunismu i vlivem necitlivého přístupu funkcionářů jich ubývalo. Poslední titul získala dvojice Adreadis, Štípek v roce 1957. To již bylo zřejmé, že československé období končí a nastupuje Asie, v té době hlavně Japonsko. Přesto se od té doby vlivem široké a kvalitní základny podařilo vychovat mnoho skvělých hráčů, kteří dobyli medaile na světových i evropských šampionátech. Pro oživení paměti opět několik jmen: Miko, Staněk, Orlowski, Kunc, Dvořáček, Panský, Voštová, Karlíková, Lužová, Grófová, Hrachová, Kasalová. Bohužel stolní tenis v českém podání stále ustupuje ze slávy. V současnosti je jediným hráčem světové špičky již stárnoucí Petr Korbel, za ním je velká výkonnostní mezera, kterou se nikomu nedaří dotáhnout.</p>

