                    <p><img class="foto foto_prava" src="img/pribehy/gymnastika/hist03.jpg" alt="foto_noh">Nářaďový tělocvik se úspěšně pěstoval i v nové sokolovně. U dorostenců tak bylo pod soustavným dohledem br. Josefa Živného. V dorosteneckém družstvu začal vynikat Josef Liška, který se pak před válkou jako muž zařadil mezi nejlepší západočeské cvičence. Po zákazu činnosti Sokola německými okupanty využili někteří naši cvičenci možnosti cvičit v DTJ Doubravka nebo DTJ Plzeň II, kde byli ochotně přijati. Cvičilo se v létě na letních cvičištích, v zimě v aule reálky na Mikulášském náměstí a vlastně všude, kam se vešel kůň na šíř. Kolem br. Lišky se utvořila skupinka mladších cvičenců, z nichž po osvobození bylo vytvořeno čtyřčlenné družstvo vyššího oddílu, které ve složení Liška, Hofman, Kučera, Kajer s úspěchem soupeřilo s nejlepšími borci této kategorie v ČOS. Z br. Lišky vyrostl dynamický, pružný a vrcholně elegantní cvičenec, který se stal čs. reprezentantem. Do naší jednoty našel cestu další vrcholový cvičenec- br. Zdeněk Růžička z Brna, který studoval v Plzni prů- myslovou školu- příkladný a oblíbený jihomoravan, stříbrný olympionik z r. 1948 v Londýně a dlouholetý československý reprezentant.</p>

                    <p>Příklady takovýchto cvičenců vzbudily v naší TJ nevšední zájem o nářaďový tělocvik i širší zájem o naši jednotu. Cvičenců vyšší výkonnosti přibývalo a hlavně- vyrůstali i dorostenci, zapálení pro gymnastiku. Z mužů můžeme jmenovat br. Veitha, Radu, Strejce, Kaisera, Krofta, Salcmana, v dorosteneckém družstvu byli m.j. Šimáček, Techl, Budín, Bartoš a další.</p>

                    <p><img class="foto foto_leva" src="img/pribehy/gymnastika/hist04.jpg" alt="foto_noh">V r. 1948 odešel br. Růžička, br. Liška dokončoval studia na vysoké škole, přesto se intenzivně cvičilo dál i přes nedobré organizační zásahy nového vedení ČOS. To už můžeme hovořit o gymnastické tradici naší jednoty. Do jednoty vstoupil nový výrazný cvičenec- br. Jiří Ivín, tehdy osmnáctiletý, houževnatý, silový typ cvičence. Ze Sokola Plzeň III přišel br. Standa Šustr, jinak také mistr ČSR v jiu-jitsu. Kolektiv doubraveckých gymnastů byl již početný a začátkem r. 1950 vybudoval gymnastickou letní tělocvičnu na zvýšeném okraji cvičiště u tehdy zděné hradby. V r. 1950 se podařilo pro závod ČOS sestavit družstvo vyššího oddílu ve složení Techl, Šimáček, Hofman, Veith, Šustr, Ivín a jako jediný z jiné jednoty Plzeňska dr. Nový z Plzně I. <br><br>Na podzim 1950 odchází na Moravu br. Liška. V září uspořádali gymnasté za účasti cvičenců z ATK Praha velikou akademii pod širým nebem, hojně navštívenou obecenstvem. V dalším období se pravidelně cvičilo; v létě venku, jinak v tělocvičně, ale začala chybět mladá krev. Ojedinělým nadějným dorostencem byl v té době obětavý Vláďa Zehle, s ním br. Bokr, Vaníček a Markuzi.</p>

                    <p><img class="foto foto_prava" src="img/pribehy/gymnastika/hist05.jpg" alt="foto_noh">Výrazným oživením gymnastické činnosti byla v létech 1953-54 pravidelná účast dvou Moravanů- Jardy Mikošky a Karla Kůstky, cvičenců vysoké úrovně, konajících v Plzni vojenskou presenční službu (br. Mikoška později působil v olympijském družstvu). Činnost v té době vrcholila akademií pod širým nebem na podzim r. 1954 za účasti ATK před hojnou návštěvou. Naši cvičenci byli často zváni na vystoupení při různých sportovních a tělocvičných akcích v blízkém i vzdálenějším okolí.Tato propagace gymnastiky však ale málokdy přinášela ovoce. <br><br><img class="foto foto_leva" src="img/pribehy/gymnastika/hist06.jpg" alt="foto_noh"> Koncem r. 1954 odcházejí br. Mikoška a Kůstka, br. Zehle jde na vojnu. Cvičební činnost se udržuje dále do 60.let, ale pro nedostatek mladých členů stagnuje a je pojímána spíše rekreačně. Snaha zaujmout sportovní gymnastikou se přesto setkala s úspěchem u některých dorostenců, jako byli např. Dlouhý, Benda, Knop, Krauz, Trnka, Mynář a další, z nichž někteří odešli posléze do sportovní základny. K širšímu rozvoji však již nedošlo a gymnastická tradice se stávala zvolna historií. Jako host u nás léta cvičíval výborný Luboš Kopecký, dnes člen jednoty, podobně i Ruda Panzer. Luboš Kopecký se dosud zúčastňuje veteránských soutěží i mezinárodních závodů a je nejzdatnějším z našich nejstarších gymnastů, kteří se dvakrát v týdnu pravidelně scházejí a zkoušejí, co se jim ještě podaří. <br><br>Kromě chlapců byla nářaďovým tělocvikem v poválečných letech zaujata i doubravecká děvčata a vytvořila kvalitní dorostenecké družstvo, jehož členkami byly Reichová, Šimáčková, sesrtry Johánkovy, Valentová, Stuchlová a další. Dvě z nich dosáhly výrazných úspěchů. Alena Reichová se vypracovala až k reprezentaci a k účasti na olympiádách, Hanka Valentová přešla brzy k odbíjené a stala se reprezentantkou a oceněnou sportovkyní. Družstvo děvčat často vystupovalo na různých veřejných podnicích a akademiích, dokud jeho členky nezavolaly rodinné povinnosti.</p>

